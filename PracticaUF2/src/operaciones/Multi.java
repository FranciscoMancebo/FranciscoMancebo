package operaciones;

public class Multi {
			
	/** Funcion de la multiplicación
	 * @param n1 Primer número flotante a multiplicar
	 * @param n2 Segundo número flotante a multiplicar
	 * @return Devuelve el resultado de la operación
	 */
	public static float multiplicacion (float n1, float n2){
		float res = 0.0f;
		res = n1 * n2;
		return res;
	}
	
}
