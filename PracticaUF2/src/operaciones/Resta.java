package operaciones;

public class Resta {
	
	/** Funcion de la resta
	 * @param n1 Primer número flotante a restar
	 * @param n2 Segundo número flotante a restar
	 * @return Devuelve el resultado de la operación
	 */
	public static float resta (float n1, float n2){
		float res = 0.0f;
		res = n1 - n2;
		return res;
	}
	
}
