package operaciones;

public class Suma {

	/** Funcion de la suma
	 * @param n1 Primer número flotante a sumar
	 * @param n2 Segundo número flotante a sumar
	 * @return Devuelve el resultado de la operación
	 */
	public static float suma (float n1, float n2){
		float res = 0.0f;
		res = n1 + n2;
		return res;
	}
}
